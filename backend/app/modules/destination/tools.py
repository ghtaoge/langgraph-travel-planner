"""目的地研究专用工具 — 城市基本信息查询"""

from langchain_core.tools import tool


@tool
def city_info_query(city: str) -> str:
    """查询某城市的基本旅游信息

    Args:
        city: 城市名

    Returns:
        城市旅游概览文本
    """
    city_data = {
        "成都": "四川省省会, 以美食(火锅、串串)、文化(武侯祠、杜甫草堂)、休闲(茶馆、麻将)闻名。最佳游览: 3-5天。消费水平: 中等。",
        "重庆": "直辖市, 以山城地貌、麻辣火锅、洪崖洞夜景闻名。最佳游览: 2-4天。消费水平: 中等偏低。",
        "西安": "陕西省会, 以兵马俑、大雁塔、回民街美食、城墙闻名。最佳游览: 3-5天。消费水平: 中等偏低。",
        "北京": "首都, 以故宫、长城、颐和园、胡同文化闻名。最佳游览: 4-7天。消费水平: 较高。",
        "上海": "直辖市, 以外滩、豫园、迪士尼、南京路闻名。最佳游览: 3-5天。消费水平: 较高。",
    }
    return city_data.get(city, f"{city}: 查询中, 推荐游览3天, 消费中等")
